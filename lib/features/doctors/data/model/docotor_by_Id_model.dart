
import 'package:gerena/features/doctors/domain/entities/finddoctors/docotor_by_id_entity.dart';

class DocotorByIdModel extends DocotorByIdEntity {
  DocotorByIdModel({
    required super.userId,
    super.nombreCompleto,
    super.nombre,
    super.apellidos,
    super.email,
    super.numeroLicencia,
    super.especialidad,
    super.experienciaTiempo,
    super.fechaNacimiento,
    super.telefono,
    super.direccion,
    super.biografia,
    super.educacion,
    super.titulo,
    super.institucion,
    super.certificacion,
    super.institucionCertificacion,
    super.foto,
    super.linkedIn,
    super.facebook,
    super.x,
    super.instagram,
    super.nombreVendedor,
    super.whatsAppVendedor,
    super.correoVendedor,
    super.puntosDisponibles,
    super.calificacion,
  });

  factory DocotorByIdModel.fromJson(Map<String, dynamic> json) {
    return DocotorByIdModel(
      userId: json["userId"] ?? 0,
      nombreCompleto: json["nombreCompleto"],
      nombre: json["nombre"],
      apellidos: json["apellidos"],
      email: json["email"],
      numeroLicencia: json["numeroLicencia"],
      especialidad: json["especialidad"],
      experienciaTiempo: json["experienciaTiempo"],
      fechaNacimiento: json["fechaNacimiento"],
      telefono: json["telefono"],
      direccion: json["direccion"],
      biografia: json["biografia"],
      educacion: json["educacion"],
      titulo: json["titulo"],
      institucion: json["institucion"],
      certificacion: json["certificacion"],
      institucionCertificacion: json["institucionCertificacion"],
      foto: json["foto"],
      linkedIn: json["linkedIn"],
      facebook: json["facebook"],
      x: json["x"],
      instagram: json["instagram"],
      nombreVendedor: json["nombreVendedor"],
      whatsAppVendedor: json["whatsAppVendedor"],
      correoVendedor: json["correoVendedor"],
      puntosDisponibles: json["puntosDisponibles"],
      calificacion: json["calificaion"] != null 
          ? (json["calificaion"] as num).toDouble()
          : null,
    );
  }

  Map<String, dynamic> toJson() => {
        "userId": userId,
        "nombreCompleto": nombreCompleto,
        "nombre": nombre,
        "apellidos": apellidos,
        "email": email,
        "numeroLicencia": numeroLicencia,
        "especialidad": especialidad,
        "experienciaTiempo": experienciaTiempo,
        "fechaNacimiento": fechaNacimiento,
        "telefono": telefono,
        "direccion": direccion,
        "biografia": biografia,
        "educacion": educacion,
        "titulo": titulo,
        "institucion": institucion,
        "certificacion": certificacion,
        "institucionCertificacion": institucionCertificacion,
        "foto": foto,
        "linkedIn": linkedIn,
        "facebook": facebook,
        "x": x,
        "instagram": instagram,
        "nombreVendedor": nombreVendedor,
        "whatsAppVendedor": whatsAppVendedor,
        "correoVendedor": correoVendedor,
        "puntosDisponibles": puntosDisponibles,
        "calificaion": calificacion,
      };
}
